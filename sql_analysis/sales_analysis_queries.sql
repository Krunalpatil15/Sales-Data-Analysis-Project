CREATE DATABASE sales_analysis;
USE sales_analysis;

CREATE TABLE sales_data (
    OrderID INT,
    Order_Date DATE,
    Region VARCHAR(50),
    Product VARCHAR(50),
    Category VARCHAR(50),
    Quantity INT,
    Sales DECIMAL(15,2)
);

SELECT COUNT(*) FROM sales_data;
SELECT * FROM sales_data;

